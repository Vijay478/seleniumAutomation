package com.ctylor.domain;

public class Items {
	
	
	int id;
	String num;
	String _class;
	String cat;
	String lastPrice;
	String stdSellunitFactor;
	String scanCode;
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String get_class() {
		return _class;
	}
	public void set_class(String _class) {
		this._class = _class;
	}
	public String getCat() {
		return cat;
	}
	public void setCat(String cat) {
		this.cat = cat;
	}
	public String getLastPrice() {
		return lastPrice;
	}
	public void setLastPrice(String lastPrice) {
		this.lastPrice = lastPrice;
	}
	public String getStdSellunitFactor() {
		return stdSellunitFactor;
	}
	public void setStdSellunitFactor(String stdSellunitFactor) {
		this.stdSellunitFactor = stdSellunitFactor;
	}
	public String getScanCode() {
		return scanCode;
	}
	public void setScanCode(String scanCode) {
		this.scanCode = scanCode;
	}
	
	
	
	

}
